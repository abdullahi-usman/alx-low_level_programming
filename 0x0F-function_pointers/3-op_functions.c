
/**
 * op_add - add two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result
 *
 */
int op_mod(int a, int b)
{
	return (a % b);
}
